create table Usuario (
	DNI Varchar(9),
	Nombre varchar(50),
	Apellidos varchar(50),
	Edad varchar(50),
	Direccion varchar(50)
);

create table curso (
	Codigo Varchar(5),
	Nombre varchar(50),
	Contraseña varchar(50),
);
create table Edificio (
	Codigo varchar(5),
	Nombre varchar(50),
	Contraseña varchar(50),
);
